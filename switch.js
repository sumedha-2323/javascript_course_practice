// dice values: 1 to 6
// this can be written using if or if and else if 
// switch

const dice = 3;
switch (dice) {
    case 1:
        console.log("You got one");
        break;
    case 2:
        console.log("You got two");
        break;
    case 3:
        console.log("You got three");
        break;
    case 4:
        console.log("You got 4");
        break;
    case 5:
        console.log("You got five");
        break;
    case 6:
        console.log("You got six");
        break;
    default:
        console.log("You didn't roll the dice");
}